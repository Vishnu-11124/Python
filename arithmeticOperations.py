# Subtraction
total_salary = 32000
house_loan = 8000
balance_amount = total_salary - house_loan
print('Balance amount: ',balance_amount)

# ------------------------------------------------------

# Multiplication
chocolate_ice_cream = 40
total_count = 6
total_price = chocolate_ice_cream * total_count
print('Total price: ',total_price)

