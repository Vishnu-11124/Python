# Division
total_students = 47
total_groups = 4
students_per_group = total_students / total_groups
print('Students per group: ',students_per_group)

# ------------------------------------------------------

# Exponentiation
power_result = 2 ** 3
print('Power result: ',power_result)

# ------------------------------------------------------

# Modulo
remainder = 10 % 3
